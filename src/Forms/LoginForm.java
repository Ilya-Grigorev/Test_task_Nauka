    public void InitListOfUserRolew() {

        int itemCount = jComboBox1.getItemCount();

        for (int i = 0; i < itemCount; i++) {
            jComboBox1.removeItemAt(0);
        }

        jComboBox1.addItem("Выберите роль текущего пользователя");

        for (Map.Entry<String, Integer> entry : UserRights.UsersRoles.entrySet()) {
            String key = entry.getKey();
            jComboBox1.addItem(key);
        }

    }

